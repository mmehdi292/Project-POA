package AppCore;


public class DataSync {
	
	public String agentName;
	public String agentType;
	public double agentNego;
	public String extra;
	
	public DataSync(String agentName, String agentType, double agentNego, String extra) {
		super();
		this.agentName = agentName;
		this.agentType = agentType;
		this.agentNego = agentNego;
		this.extra = extra;
	}
	
}
